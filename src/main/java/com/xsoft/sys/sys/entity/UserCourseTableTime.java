package com.xsoft.sys.sys.entity;

import java.util.Date;

import com.xsoft.base.annotation.Key;


public class UserCourseTableTime {
	@Key
	private Integer	course_time_id;
	
	private Integer course_id;
	
	private Date start_time;
	
	private Date end_time;
	
	public Integer getCourse_time_id() {
		return course_time_id;
	}
	public Integer getCourse_id() {
		return course_id;
	}
	public Date getStart_time() {
		return start_time;
	}
	public Date getEnd_time() {
		return end_time;
	}
	public void setCourse_time_id(Integer course_time_id) {
		this.course_time_id = course_time_id;
	}
	public void setCourse_id(Integer course_id) {
		this.course_id = course_id;
	}
	public void setStart_time(Date start_time) {
		this.start_time =start_time;
	}
	public void setEnd_time(Date end_time) {
		this.end_time = end_time;
	}

}